package org.neodatis.odb.test.query.criteria;

public class ClassWithLong {
	private long i;
	private String name;

	public ClassWithLong(long i, String name) {
		super();
		this.i = i;
		this.name = name;
	}

	public long getI() {
		return i;
	}

	public void setI(long i) {
		this.i = i;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
